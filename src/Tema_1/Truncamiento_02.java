

public class Truncamiento_02 {
    public static void main(String[] args) {
        int a = 7;
        int b = 2;
        int result = a / b;
        double preciseResult = (double) a / b;
        System.out.println("División entera: " + a + " / " + b + " = " + result);
        System.out.println("División precisa: " + a + " / " + b + " = " + preciseResult);
    }
}
